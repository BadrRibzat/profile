// Enhanced translation function for PDF overlay
export const translateText = (text, targetLang, sourceLang, documentId) => {
  if (targetLang === sourceLang || !text || text.trim() === '') {
    return null;
  }
  
  // Get document-specific translations
  const docTranslations = documentTranslations[documentId]?.translations || {};
  
  // Look for exact match first
  if (docTranslations[text] && docTranslations[text][targetLang]) {
    return docTranslations[text][targetLang];
  }
  
  // Look for partial matches (for longer text containing key phrases)
  for (const [key, translations] of Object.entries(docTranslations)) {
    if (text.includes(key) && translations[targetLang]) {
      return text.replace(key, translations[targetLang]);
    }
  }
  
  return null;
};
